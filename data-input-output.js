/*
    Vamos utilizar a função prompt para capturar o valor inserido pelo usuário
*/

var nome = prompt('Digite seu nome');
var idade = prompt('Digite sua idade');
var profissao = prompt('Digite sua profissão');

/*
    Operador de concatenação: +
    Vamos formar uma frase unindo essas variáveis com um string
*/ 
document.write('Olá ' + nome + ', você tem ' + idade + ' de idade e sua profissão é ' + profissao);

